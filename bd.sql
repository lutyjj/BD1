  CREATE OR REPLACE TRIGGER "BD"."LOG_STUDENT" 
    AFTER UPDATE OF faculty_id ON students
    FOR EACH ROW
BEGIN
    IF :old.faculty_id IS NOT NULL THEN
        INSERT INTO STUDENTS_LOG(student_id, starting_date, ending_date, faculty_id)
        VALUES (:old.student_id, :new.starting_date, sysdate, :old.faculty_id);
    END IF;
END;
